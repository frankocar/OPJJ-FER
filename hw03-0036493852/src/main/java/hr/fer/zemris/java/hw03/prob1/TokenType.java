/**
 * An enum describing possible token types
 * 
 * @author Franko Car
 *
 */
public enum TokenType {

	/**
	 * End of file token 
	 */
	EOF, 
	/**
	 * A word token
	 */
	WORD, 
	/**
	 * A number token
	 */
	NUMBER,
	/**
	 * A symbol token
	 */
	SYMBOL;
	
}
